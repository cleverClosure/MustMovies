import UIKit

extension UIFont {
    static func headingFont() -> UIFont {
        return UIFont.systemFont(ofSize: 30, weight: UIFont.Weight.bold)
    }
    static func subTitleFont() -> UIFont {
        return UIFont.systemFont(ofSize: 20, weight: UIFont.Weight.medium)
    }
}

